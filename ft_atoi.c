#include "libft.h"

int	ft_atoi(const char *str)
{
	long long	num;
	int			sign;
	int			i;

	sign = 1;
	i = 0;
	num = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		num = (num * 10) + (str[i] - 48);
		i++;
	}
	num *= sign;
	return (num);
}
